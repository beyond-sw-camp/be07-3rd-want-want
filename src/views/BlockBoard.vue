    setup() {
        const route = useRoute();
        const router = useRouter();
        const blockId = ref(route.params.blockId)
        const showMapModal = ref(false);
        const localBlock = ref({
            title: '',
            category: '',
            content: '',
            placeName: '',
            heartCount: 0,
            comments: null, // Ï¥àÍ∏∞Í∞íÏùÑ nullÎ°ú ÏÑ§Ï†ï
            startTime: null,
