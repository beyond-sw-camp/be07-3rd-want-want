        </div>
        <v-form>
            <div class="form-floating" style="margin: 20px 0 40px">
                <textarea class="form-control" v-model="localBlock.content" placeholder="ÎÇ¥Ïö©ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî" required
                    style="height: 300px;"></textarea>
