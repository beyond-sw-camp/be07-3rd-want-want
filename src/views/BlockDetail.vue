                    <div class="photo-container">
                        <v-img :src="photo.url" alt="Î∏îÎ°ù Ïù¥ÎØ∏ÏßÄ" class="slider-image"></v-img>
                        <span class="material-symbols-outlined delete-btn" @click="handleDelete(photo.id)">
                            delete
