<script>
import axios from 'axios';
import GoogleMap from "@/components/GoogleMap.vue";
import CustomModal from "@/components/CustomModal.vue";
import { ref, onMounted, computed } from 'vue';
import { useRoute, useRouter } from 'vue-router';

export default {
    data() {
        return {
            categoryMap: {
                SPOT: "Î™ÖÏÜå",
                RESTAURANT: "ÏãùÎãπ",
                CAFE: "Ïπ¥Ìéò",
                ETC: "Í∏∞ÌÉÄ"
            },
            categoryColors: {
                SPOT: [255, 182, 193],
                CAFE: [255, 180, 110],
                RESTAURANT: [173, 216, 230],
                ETC: [192, 192, 192],
            },

        }
    },
    computed: {
        translatedCategories() {
            return Object.keys(this.categoryMap).map(category => ({
                category,
                label: this.categoryMap[category],
                color: this.categoryColors[category]
            }));
        },
        filteredCategories() {
            const filtered = this.translatedCategories.filter(item => item.label === this.localBlock.category);
            console.log('Filtered Categories:', filtered); // ÎîîÎ≤ÑÍπÖÏùÑ ÏúÑÌïú Î°úÍ∑∏
            return filtered;
        },
        filteredPhotos() {
            // ÏÇ≠Ï†úÌï† ÏÇ¨ÏßÑÏùÑ Ï†úÏô∏Ìïú ÏÇ¨ÏßÑÎßå ÌïÑÌÑ∞ÎßÅ
            return this.blockPhotos.filter(photo => !this.delFiles.includes(photo.id))
                .concat(this.newFiles.map(file => ({ url: URL.createObjectURL(file), id: file.name })));
        }
    },
    components: { CustomModal, GoogleMap },
    setup() {
        const route = useRoute();
        const router = useRouter();
        const blockId = ref(route.params.blockId)
        const showMapModal = ref(false);
        const localBlock = ref({
            title: '',
            category: '',
            content: '',
            placeName: '',
            heartCount: 0,
            startTime: null,
            endTime: null,
            latitude: null,
            longitude: null,
        });
        const blockPhotos = ref([]);
        const activeIndex = ref(0);

        const valid = ref(true);
        const startDateMenu = ref(false);
        const endDateMenu = ref(false);
        const selectedBlock = ref(null);

        const newFiles = ref([]); // ÏÉàÎ°ú Ï∂îÍ∞ÄÎêú ÌååÏùº
        const delFiles = []; // ÏÇ≠Ï†úÎêú ÌååÏùº URL

        const categoryMap = {
            SPOT: "Î™ÖÏÜå",
            RESTAURANT: "ÏãùÎãπ",
            CAFE: "Ïπ¥Ìéò",
            ETC: "Í∏∞ÌÉÄ"
        };
        const reverseCategoryMap = {
            "Î™ÖÏÜå": "SPOT",
            "ÏãùÎãπ": "RESTAURANT",
            "Ïπ¥Ìéò": "CAFE",
            "Í∏∞ÌÉÄ": "ETC"
        };

        const fetchBlock = async () => {
            try {
                const blockId = route.params.blockId;
                const response = await axios.get(`${process.env.VUE_APP_API_BASE_URL}/api/v1/block/${blockId}/detail`);
                const blockData = response.data.result;
                console.log(blockData);
                localBlock.value = {
                    ...blockData,
                    category: categoryMap[blockData.category] || blockData.category,
                };
            } catch (error) {
                console.error('Î∏îÎ°ù Ï†ïÎ≥¥Î•º Í∞ÄÏ†∏Ïò§Îäî Ï§ë Ïò§Î•ò Î∞úÏÉù:', error);
            }
        };

        const updateBlock = async () => {
            if (valid.value) {
                try {
                    const categoryInEnglish = reverseCategoryMap[localBlock.value.category] || localBlock.value.category;

                    await axios.patch(`${process.env.VUE_APP_API_BASE_URL}/api/v1/block/${selectedBlock.value}/update`, {
                        ...localBlock.value,
                        category: categoryInEnglish,
                        latitude: localBlock.value.latitude,
                        longitude: localBlock.value.longitude,
                    });
                    await updatePhoto();

                    alert('Î∏îÎ°ùÏù¥ ÏÑ±Í≥µÏ†ÅÏúºÎ°ú ÏóÖÎç∞Ïù¥Ìä∏ÎêòÏóàÏäµÎãàÎã§.');
                    router.push(`/block/${localBlock.value.blockId}/board`);
                } catch (error) {
                    console.error('Î∏îÎ°ù ÏóÖÎç∞Ïù¥Ìä∏ Ï§ë Ïò§Î•ò Î∞úÏÉù:', error);
                    alert('Î∏îÎ°ù ÏóÖÎç∞Ïù¥Ìä∏ Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
                }
            }
        };

        const cancel = () => {
            router.push(`/block/${localBlock.value.blockId}/board`);
        };

        const deleteBlock = async () => {
            if (confirm('Ï†ïÎßêÎ°ú Ïù¥ Î∏îÎ°ùÏùÑ ÏÇ≠Ï†úÌïòÏãúÍ≤†ÏäµÎãàÍπå?')) {
                try {
                    await axios.delete(`${process.env.VUE_APP_API_BASE_URL}/api/v1/block/${selectedBlock.value}/delete`);
                    alert('Î∏îÎ°ùÏù¥ ÏÑ±Í≥µÏ†ÅÏúºÎ°ú ÏÇ≠Ï†úÎêòÏóàÏäµÎãàÎã§.');
                    router.push(`/project/${localBlock.value.projectId}/board`);
                } catch (error) {
                    console.error('Î∏îÎ°ù ÏÇ≠Ï†ú Ï§ë Ïò§Î•ò Î∞úÏÉù:', error);
                    alert('Î∏îÎ°ù ÏÇ≠Ï†ú Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
                }
            }
        }

        const handlePlaceSelected = (place) => {
            console.log(place)
            localBlock.value.placeName = place.name;
            localBlock.value.latitude = place.lat;
            localBlock.value.longitude = place.lng;
        };

        // ÏÇ¨ÏßÑ Í¥ÄÎ†® Î°úÏßÅ
        const getPhotos = async () => {
            try {
                const blockId = route.params.blockId;
                const response = await axios.get(`${process.env.VUE_APP_API_BASE_URL}/api/v1/photo/${blockId}/list`);
                blockPhotos.value = response.data.result.photoList;
                console.log(blockPhotos.value);
            } catch (e) {
                console.log(e);
            }
        };

        const filteredPhotos = computed(() => {
            const delFileUrls = new Set(delFiles.value);
            return [
                ...blockPhotos.value.filter(photo => !delFileUrls.has(photo.id)),
                ...newFiles.value.map(file => ({ url: URL.createObjectURL(file), id: file.name })) // ÏÉàÎ°ú Ï∂îÍ∞ÄÎêú ÏÇ¨ÏßÑ
            ];
        });

        const nextSlide = () => {
            activeIndex.value = (activeIndex.value + 1) % (filteredPhotos.value.length + 1);
            updateSliderPosition();
        };

        const prevSlide = () => {
            activeIndex.value = (activeIndex.value - 1 + filteredPhotos.value.length) % filteredPhotos.value.length;
            updateSliderPosition();
        };

        const updateSliderPosition = () => {
            const slider = document.querySelector('.slider');
            const offset = -activeIndex.value * 500; // Ïù¥ÎØ∏ÏßÄ ÌÅ¨Í∏∞ÏôÄ ÎèôÏùºÌïú ÎÑàÎπÑÎ°ú Ïò§ÌîÑÏÖã Í≥ÑÏÇ∞
            slider.style.transform = `translateX(${offset}px)`;
        };

        const triggerFileUpload = () => {
            document.querySelector("input[type='file']").click();
        };

        const handleFileUpload = (event) => {
            const files = event.target.files;
            if (files.length > 0) {
                for (let i = 0; i < files.length; i++) {
                    newFiles.value.push(files[i]);
                }
            }
            console.log(newFiles.value);
        };

        const handleDelete = (id) => {
            // Í∏∞Ï°¥ Î∏îÎ°ùÏóê ÏûàÎäî ÏÇ¨ÏßÑÏùÑ ÏÇ≠Ï†úÌï† Îïå
            const isExistingPhoto = blockPhotos.value.some(photo => photo.id === id);
            if (isExistingPhoto) {
                blockPhotos.value = blockPhotos.value.map(photo =>
                    photo.id === id ? { ...photo, isDeleted: true } : photo
                );
                delFiles.push(id);
                console.log("delFiles", delFiles);
            } else {
                // ÏÉàÎ°ú Ï∂îÍ∞ÄÎêú ÏÇ¨ÏßÑ(newFiles)Ïóê Ìï¥ÎãπÌïòÎäî Í≤ΩÏö∞, newFilesÏóêÏÑúÎßå ÏÇ≠Ï†ú
                newFiles.value = newFiles.value.filter(file => file.name !== id);
                console.log("newFiles", newFiles.value);
            }
        };

        const updatePhoto = async () => {
            try {
                const formData = new FormData();
                formData.append('blockId', selectedBlock.value);

                if (delFiles.length > 0 && newFiles.value.length === 0) {
                    // ÏÇ≠Ï†úÎßå ÏûàÎäî Í≤ΩÏö∞
                    delFiles.forEach(fileId => formData.append('delFiles', fileId));

                    const response = await axios.put(`${process.env.VUE_APP_API_BASE_URL}/api/v1/photo/delete`, formData, {
                        headers: {
                            'Content-Type': 'multipart/form-data'
                        }
                    });
                    console.log('ÏÇ¨ÏßÑ ÏÇ≠Ï†ú ÏÑ±Í≥µ:', response.data);

                } else if (delFiles.length === 0 && newFiles.value.length > 0) {
                    // Ï∂îÍ∞ÄÎßå ÏûàÎäî Í≤ΩÏö∞
                    newFiles.value.forEach(file => formData.append('files', file));

                    const response = await axios.post(`${process.env.VUE_APP_API_BASE_URL}/api/v1/photo/upload`, formData, {
                        headers: {
                            'Content-Type': 'multipart/form-data'
                        }
                    });
                    console.log('ÏÇ¨ÏßÑ Ï∂îÍ∞Ä ÏÑ±Í≥µ:', response.data);

                } else if (delFiles.length > 0 && newFiles.value.length > 0) {
                    // ÏÇ≠Ï†úÏôÄ Ï∂îÍ∞Ä Î™®Îëê ÏûàÎäî Í≤ΩÏö∞
                    delFiles.forEach(fileId => formData.append('delFiles', fileId));
                    newFiles.value.forEach(file => formData.append('newFiles', file));

                    const response = await axios.put(`${process.env.VUE_APP_API_BASE_URL}/api/v1/photo/update`, formData, {
                        headers: {
                            'Content-Type': 'multipart/form-data'
                        }
                    });
                    console.log('ÏÇ¨ÏßÑ ÏóÖÎç∞Ïù¥Ìä∏ ÏÑ±Í≥µ:', response.data);
                }

            } catch (error) {
                console.error('ÏÇ¨ÏßÑ Ï≤òÎ¶¨ Ï§ë Ïò§Î•ò Î∞úÏÉù:', error);
                alert('ÏÇ¨ÏßÑ Ï≤òÎ¶¨ Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
            }
        };

        onMounted(async () => {
            selectedBlock.value = route.params.blockId;
            await fetchBlock();
            await getPhotos();
        });

        return {
            showMapModal,
            localBlock,
            valid,
            startDateMenu,
            endDateMenu,
            selectedBlock,
            blockId,
            fetchBlock,
            updateBlock,
            cancel,
            deleteBlock,
            handlePlaceSelected,
            blockPhotos,
            nextSlide,
            prevSlide,
            triggerFileUpload,
            handleFileUpload,
            handleDelete,
            delFiles,
            newFiles,
            activeIndex,
            updatePhoto,
        };
    },
    methods: {
        async toggleLike(localBlock) {
            const wasLiked = localBlock.isHearted;
            const newHeartCount = wasLiked ? localBlock.heartCount - 1 : localBlock.heartCount + 1;
            localBlock.isHearted = !wasLiked;
            localBlock.heartCount = newHeartCount;
            try {
                await axios.post(
                    `${process.env.VUE_APP_API_BASE_URL}/api/v1/block/${localBlock.blockId}/heart`,
                    {}, // Îπà Î≥∏Î¨∏ÏúºÎ°ú ÏöîÏ≤≠
                    {
                        headers: {
                            Authorization: `Bearer ${localStorage.getItem("token")}`, // ÌÜ†ÌÅ∞ÏùÑ Ìó§ÎçîÏóê Ìè¨Ìï®
                        },
                    }
                );
            } catch (error) {
                console.error("Ï¢ãÏïÑÏöî ÏóÖÎç∞Ïù¥Ìä∏ Ï§ë Ïò§Î•ò Î∞úÏÉù:", error);
                // ÏóÖÎç∞Ïù¥Ìä∏ Ïã§Ìå® Ïãú Î°úÏª¨ÏóêÏÑú Î≥ÄÍ≤Ω ÏÇ¨Ìï≠ ÎêòÎèåÎ¶¨Í∏∞
                localBlock.isHearted = wasLiked;
                localBlock.heartCount = wasLiked ? localBlock.heartCount + 1 : localBlock.heartCount - 1;
            }
        },
        getStyle() {
            return {
                backgroundColor: localStorage.getItem('backgroundColor'),
                padding: "20px",
                margin: "10px 0",
                borderRadius: "10px !important",
                color: "#000",
            };
        },
    }
};
</script>

